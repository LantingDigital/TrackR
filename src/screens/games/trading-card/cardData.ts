/**
 * Get all unlocked cards for initial collection
 */
export const getStarterCards = (): CoasterCard[] => {
  return ALL_CARDS.filter(card => card.isUnlocked);
};

/**
 * Get card by ID
 */
export const getCardById = (id: string): CoasterCard | undefined => {
  return ALL_CARDS.find(card => card.id === id);
};

/**
 * Get cards by rarity
 */
export const getCardsByRarity = (rarity: CardRarity): CoasterCard[] => {
  return ALL_CARDS.filter(card => card.rarity === rarity);
};
